package com.string.pack;

public class stringTest 
{

	public static void main(String[] args) 
	{

		// num	= b,a,l,a,j,i
		//		  0,1,2,3,4,5  ---> index
		
		String s="balaji";
		System.out.println(s); // balaji
		System.out.println(s.length()); // 6 --> total length of name(b,a,l,a,j,i) =6
		System.out.println(s.charAt(3)); // a
		System.out.println(s.charAt(5)); //i
	}

}
